
public class Agendamento {
	public String dataAgendamento;
	public String razaoAgendamento; 
	
	Agendamento() {}
	
	
	public String getDataAgendamento() {
		return dataAgendamento;
	}

	public void setDataAgendamento(String dataAgendamento) {
		this.dataAgendamento = dataAgendamento;
	}

	public String getRazaoAgendamento() {
		return razaoAgendamento;
	}

	public void setRazaoAgendamento(String razaoAgendamento) {
		this.razaoAgendamento = razaoAgendamento;
	}

}
